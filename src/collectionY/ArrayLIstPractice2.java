package collectionY;
/*
存储自定义对象并遍历
 */
public class ArrayLIstPractice2 {
    private String name;
    private  int age;

    public ArrayLIstPractice2(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
